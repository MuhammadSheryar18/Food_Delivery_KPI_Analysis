-- GMV Analysis

-- Question:
-- What is the total Gross Merchandise Value (GMV) by day, week, and month?

-- Why this matters:
-- Tracks revenue growth and seasonality.

-- 2️⃣ Repeat Customers

-- Question:
-- What percentage of users place more than one order?

-- Why this matters:
-- Measures customer loyalty and product stickiness.

-- 3️⃣ Average Order Value (AOV)

-- Question:
-- What is the average order value by city and by restaurant?

-- Why this matters:
-- Identifies high-value locations and partners.

-- 4️⃣ Revenue Concentration

-- Question:
-- Which restaurants contribute to the top 20% of total GMV?

-- Why this matters:
-- Highlights dependency risk and key revenue drivers.

-- 5️⃣ Retention Analysis

-- Question:
-- What is user retention after 7, 14, and 30 days from their first order?

-- Why this matters:
-- Evaluates user engagement over time.

-- 6️⃣ Churn Rate

-- Question:
-- What is the monthly user churn rate?

-- Why this matters:
-- Shows how many users stop ordering month-over-month.

-- 7️⃣ High-Frequency, Low-Value Users

-- Question:
-- Which users place frequent orders but contribute low total GMV?

-- Why this matters:
-- Helps identify discount abuse or pricing inefficiencies.